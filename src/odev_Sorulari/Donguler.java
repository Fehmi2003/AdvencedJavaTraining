package odev_Sorulari;

public class Donguler {
    public static void main(String[] args) {

        int sayi=10;
        while (sayi<15){
            ++sayi;
            System.out.println(sayi);
        }
        System.out.println("********************");
        //Kodu bir kere çalıştır ondan sonra şartı kontrol et = do while
        int s1=10;
        do {
            System.out.println("selam");
            s1++;
        }
        while (s1<20);
    }
}
